package control_statement;

public class If_else_Study {

	public static void main(String[] args) {
		
		//If my marks greater than or equal to 40 then i will be pass else i will be fail
		
		
		int marks=40;
		
		if (marks>=40) //True/False
		{
			System.out.println("I am Pass");
		}
		
		else
		{
			System.out.println("I am failed");
		}

	}

}
