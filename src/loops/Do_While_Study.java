package loops;

public class Do_While_Study {

	public static void main(String[] args) {
		
		// Want to print velocity 3 times
		
		int i=1;
		
		do
		{
			System.out.println("Velocity");
			i++;
		}
      while (i<=3);
    	  
		// I want to print numbers 1 to 50
		
		int j=50;
		do
		{
			System.out.println(j);
			j--;
		}
		while (j>=1);
	}

}
