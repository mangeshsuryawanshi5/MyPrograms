package loops;

public class For_loop_Study {

//	
//	syntax for For loop
//	for (initialization; declaration; updation)
	
	
	// I want to print Velocity five times
	
	public static void main(String[] args) {
		
 for (int i=1; i<=5; i++)
 {
	 System.out.println("Velocity");
	 
	 for (int j=1; j>=5; j--)
	 {
		 System.out.println("Velocity");
	 }
	 
 }
 }
 }
