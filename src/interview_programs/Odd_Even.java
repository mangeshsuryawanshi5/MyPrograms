package interview_programs;

public class Odd_Even {

	public static void main(String[] args) {
	
		for(int i=1;i<=20;i++)
		{
			if(i%2==1)
			{
				System.out.println(i+" "+ "is odd number");
			}
		}

		System.out.println("=======================");
		for(int i=1;i<=20;i++)
		{
			if(i%2==0)
			{
				System.out.println(i+" "+ "is even number");
			}
		}
          
          }
	
    }

          


